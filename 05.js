let num1=document.getElementById("num1");
let num2=document.getElementById("num2");
function add(){
    let r=document.getElementById("res");
    r.value=Number();
    r.value=(Number(num1.value)+Number(num2.value));
}
function sub(){
    let r=document.getElementById("res");
    r.value=Number();
    r.value=(Number(num1.value)-Number(num2.value));
}
function mult(){
    let r=document.getElementById("res");
    r.value=num1.value*num2.value;
}
function div(){
    let r=document.getElementById("res");
    r.value=Number();
    r.value=(Number(num1.value)/Number(num2.value));
}
function rem(){
    let r=document.getElementById("res");
    r.value=Number();
    r.value=(Number(num1.value)%Number(num2.value));
}






































   















































